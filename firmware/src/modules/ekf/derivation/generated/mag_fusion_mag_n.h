    K[10] = x35*(P(10, 0)*x15 - P(10, 1)*x16 - P(10, 11)*x5 - P(10, 18)*x7 + P(10, 2)*x17 - P(10, 20)*x5 - P(10, 9)*x7 - x18 - x8);
    K[11] = x35*(P(11, 0)*x15 - P(11, 1)*x16 + 2*P(11, 10)*x1 - P(11, 18)*x7 + 2*P(11, 19)*x1 + P(11, 2)*x17 - P(11, 9)*x7 - x20 - x22);
    K[12] = x35*(P(12, 0)*x15 - P(12, 1)*x16 + 2*P(12, 10)*x1 - P(12, 11)*x5 - P(12, 18)*x7 + 2*P(12, 19)*x1 + P(12, 2)*x17 - P(12, 20)*x5 - P(12, 9)*x7);
    K[13] = x35*(P(13, 0)*x15 - P(13, 1)*x16 + 2*P(13, 10)*x1 - P(13, 11)*x5 - P(13, 18)*x7 + 2*P(13, 19)*x1 + P(13, 2)*x17 - P(13, 20)*x5 - P(13, 9)*x7);
