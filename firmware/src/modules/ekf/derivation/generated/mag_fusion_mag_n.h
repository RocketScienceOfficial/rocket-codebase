    const float x33 = 2*x1;
    const float x34 = -2*x4;
    const float x35 = 1.0/x31;
    *innov = meas - x0*x2 + x3*x5 + x6*x7;
    *innov_var = x31;
    H[0] = x15;
    H[1] = -x16;
    H[2] = x17;
    H[3] = 0;
    H[4] = 0;
    H[5] = 0;
    H[6] = 0;
    H[7] = 0;
    H[8] = 0;
    H[9] = x32;
    H[10] = x33;
    H[11] = x34;
    H[12] = 0;
    H[13] = 0;
    H[14] = 0;
    H[15] = 0;
    H[16] = 0;
    H[17] = 0;
    H[18] = x32;
    H[19] = x33;
    H[20] = x34;
    K[0] = x35*(-P(0, 1)*x16 + 2*P(0, 10)*x1 - P(0, 11)*x5 - P(0, 18)*x7 + 2*P(0, 19)*x1 + P(0, 2)*x17 - P(0, 20)*x5 - P(0, 9)*x7 - x28);
    K[1] = x35*(P(1, 0)*x15 + 2*P(1, 10)*x1 - P(1, 11)*x5 - P(1, 18)*x7 + 2*P(1, 19)*x1 + P(1, 2)*x17 - P(1, 20)*x5 - P(1, 9)*x7 - x30);
    K[2] = x35*(P(2, 0)*x15 - P(2, 1)*x16 + 2*P(2, 10)*x1 - P(2, 11)*x5 - P(2, 18)*x7 + 2*P(2, 19)*x1 - P(2, 20)*x5 - P(2, 9)*x7 - x29);
    K[3] = x35*(P(3, 0)*x15 - P(3, 1)*x16 + 2*P(3, 10)*x1 - P(3, 11)*x5 - P(3, 18)*x7 + 2*P(3, 19)*x1 + P(3, 2)*x17 - P(3, 20)*x5 - P(3, 9)*x7);
    K[4] = x35*(P(4, 0)*x15 - P(4, 1)*x16 + 2*P(4, 10)*x1 - P(4, 11)*x5 - P(4, 18)*x7 + 2*P(4, 19)*x1 + P(4, 2)*x17 - P(4, 20)*x5 - P(4, 9)*x7);
    K[5] = x35*(P(5, 0)*x15 - P(5, 1)*x16 + 2*P(5, 10)*x1 - P(5, 11)*x5 - P(5, 18)*x7 + 2*P(5, 19)*x1 + P(5, 2)*x17 - P(5, 20)*x5 - P(5, 9)*x7);
    K[6] = x35*(P(6, 0)*x15 - P(6, 1)*x16 + 2*P(6, 10)*x1 - P(6, 11)*x5 - P(6, 18)*x7 + 2*P(6, 19)*x1 + P(6, 2)*x17 - P(6, 20)*x5 - P(6, 9)*x7);
